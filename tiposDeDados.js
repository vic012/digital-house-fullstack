//Strings usam ("") ou ('')
let nome = "Pedro";
console.log(nome);

//Inteiro
let idade = 25;
console.log(idade);

//Decimal
let altura = 1.82;
console.log(altura);

//Booleano ou false
let diaChuvoso = true;
let diaQuente = false;
console.log(diaChuvoso);
console.log(diaQuente);

//NaN Não é um número
let naoNumero = NaN;
console.log(naoNumero);

//Null Valor nulo ou não se sabe o dado preciso
let jogadores = null;
console.log(jogadores);

//Undefined valor indefinido que pode ser definido (atribuido) depois
let espectadores = undefined;
console.log(espectadores);
espectadores = 20;
console.log(espectadores);

//Objeto literal são valores dentro de {}
//let carro = {
//    propriedade: valor
//}
let carro = {
    tipo: "Celta",
    cor: "Preto"
};
console.log(carro);
console.log(carro.tipo);
console.log(carro.cor);

//Array lista de dados
let moto = [
    'Honda',
    'Roxo'
];
console.log(moto);
console.log(moto[0]);
console.log(moto[1]);